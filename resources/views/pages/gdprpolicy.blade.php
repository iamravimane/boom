   <h4>GDPR POLICY</h4>

   <p><strong>Introduction</strong></p>
   <p>The data-driven world around us is witnessing an Information Revolution, where the technocrats are generating, capturing,
      and processing enormous data about people, their location, products or services. Its the need of an hour to incorporate
      important concepts and principles into our already defined data management policies.</p>
   <p>In order to address the key issues, the General Data Protection Regulation (GDPR) has been deployed across the European
      Union as of 25<sup>th</sup>May 2018. A world reshaped by data must define data governance, data processing activities,
      and data compliance (who owns it, uses it and how its protected). As a result, every organization that collects, processes
      or stores personal data should be taking steps now to ensure it can achieve compliance.</p>
   <p>The GDPR has been designed to meet the requirements of the digital age by updating the myriad national data protection
      laws currently in place with a cohesive set of rules. The new Regulation aims to standardize data protection laws and
      processing across the EU; affording individuals stronger, more consistent rights to access and control their personal
      information.</p>
   <p><strong>Our Commitment to Compliance</strong></p>
   <p>Big Market Research (we or us or our) is committed to high standards of information security, data privacy, and transparency,
      and to managing data in covenant with legislation and regulation, including but not limited to GDPR. At Big Market
      Research, we value our customers success, and understand the need of a complaint and consistent approach to data protection.
      We have always been dedicated to safeguarding the personal information of our users under our remit. However, we recognize
      our obligations in continuously updating and expanding this program to keep meet the demands of the GDPR.</p>
   <p><strong>Gearing-up for the GDPR</strong></p>
   <p>The GDPR imposes new rules on organizations in the European Union (EU) and those that offer goods and services to people
      in the EU, or that collect and analyze data tied to EU residents, no matter where they are located.</p>
   <p>Big Market Research focuses on following facets while preparing for GDPR:</p>
   <ul>
      <li>Building on existing security and business continuity management policies, processes and controls, with enhanced personal
         privacy rights to ensure compliance.</li>
      <li>Performing gap and privacy assessment to support GDPR compliance for its customers, with mandatory breach reporting
         and significant penalties for non-compliance.</li>
      <li>Increased duty for protecting data, developing compliance plans and building a stronger secure platform for the customers
         by taking control of their data and reviewing their deployment options.</li>
      <li>Provision of services to help customers to understand and prepare for GDPR.</li>
      <li>Making amendments in all our data contracts to meet additional requirements introduced by the GDPR.</li>
      <li>Working towards deploying a dedicated erasure procedure to meet the new Right to Erasure obligation, while also assessing
         how long we retain and store data. The company is quite perceptive on when this and other data subjects rights apply;
         along with any exemptions, response timeframes and notification responsibilities.</li>
      <li>Training the workforce on the enhanced data rights given to individuals by the GDPR. All staff, be it sales or security,
         needs to be aware of key changes, such as no longer posting charges for responding to subject access requests.</li>
      <li>Upgrading procedures and safeguarding measures to secure, encrypt and maintain the integrity of the data, especially
         in regard to International Data Transfers &amp; Third-Party Disclosures.</li>
      <li>Renovating processes for recording consent, to ensure that we can evidence an affirmative opt-in, along with time and
         date records; and an easy to use mechanism to withdraw consent at any time.</li>
   </ul>
